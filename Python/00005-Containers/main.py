### Python Containers

# list : mutable ordered container that can hold multiple datatypes.
list = [1, 2, "str", 4 ,5 ,6]

# tuple : immutable ordered container that can hold multiple datatypes.
tupel = (1, 2, 3.3, 4, "name", 6)

# dictionary : mutable unordered container the holds pairs of key and value that con be of multiple datatypes.
dict = {1:"a", "b":2, 3:"c", 4.3:4}

# set : mutable unordered container that holds unique hasable datatypes.  
set = {1, 2, 3, 4, 5, 6}

# frozenset : and immutable set.
frozenset = frozenset({1, 2, 3, 4, 5})

# print the values.
print(list)
print(tupel)
print(dict)
print(set)
print(frozenset)

# types.
print(type(list))
print(type(tupel))
print(type(dict))
print(type(set))
print(type(frozenset))
